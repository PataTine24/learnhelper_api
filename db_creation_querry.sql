CREATE DEFINER=`root`@`localhost` PROCEDURE `add_new_random_question_to_test`(
    IN t_id INT
)
BEGIN
    DECLARE q_id INT DEFAULT 0;
    DECLARE questions_available BOOLEAN DEFAULT FALSE;

    -- Check if the maximum number of questions has been reached
    SELECT
        COUNT(`con_test_question`.`question_id`) >= `taken_tests`.`number_of_questions`
    INTO questions_available
    FROM
        `con_test_question`
    LEFT JOIN
        `taken_tests`
    ON
        `con_test_question`.`test_id` = `taken_tests`.`id`
    WHERE
        `con_test_question`.`test_id` = t_id
    GROUP BY
        `con_test_question`.`test_id`;

    -- If the maximum number of questions is not reached
    IF questions_available = FALSE THEN

        -- Get the test type from the taken_tests table; if not found, return q_id = 0
        SELECT IFNULL(
            (SELECT `test_type_id`
            FROM `taken_tests`
            WHERE `id` = t_id
            LIMIT 1), 0)
        INTO @t_type_id;

        IF @t_type_id != 0 THEN

            -- Find a new question that hasn't been added to the test yet
            SELECT IFNULL(
                (SELECT `id`
                FROM `questions`
                WHERE `validated_for_tests`=1 AND
					`id` NOT IN
                    (SELECT `question_id`
                    FROM `con_test_question`
                    WHERE `test_id` = t_id)
                AND (`test_type_id` = @t_type_id OR
                     `test_type_id` IN (
                         SELECT `id`
                         FROM `test_types`
                         WHERE `main_type_id` = @t_type_id
                     ))
                ORDER BY RAND()
                LIMIT 1), 0)
            INTO q_id;

            -- If a question was found, insert it into con_test_question
            IF q_id != 0 THEN
                INSERT INTO `con_test_question`(`test_id`, `question_id`)
                VALUES(t_id, q_id);
            END IF;

        END IF;

    END IF;

    -- Return the question id, which can be 0 if test id was wrong or no question was found
    SELECT q_id;

END ;;
